export default function BottomNav() {
  const { t } = useTranslation()
  const { getWordsDue, words } = useDifficultWordsStore()
  const dueCount = words.length > 0 ? getWordsDue().length : 0
  const location = useLocation()

  return (
    <nav className="lg:hidden fixed bottom-0 inset-x-0 z-50">
      <div className="card !rounded-none border-t border-th-border">
        <div className="flex items-stretch pb-[env(safe-area-inset-bottom)]">
          {tabs.map((tab) => {
            const isActive = tab.to === '/'
              ? location.pathname === '/'
              : tab.matchPaths.some(p => location.pathname.startsWith(p))

            return (
              <NavLink
                key={tab.to}
                to={tab.to}
                className={`relative flex-1 flex flex-col items-center gap-0.5 py-2.5 min-h-[52px] transition-colors ${
                  isActive ? 'text-th-accent' : 'text-th-muted'
                }`}
              >
                {isActive && (
                  <span className="absolute top-0 left-1/2 -translate-x-1/2 w-8 h-0.5 rounded-full bg-th-accent" />
                )}
                <span className="relative">
                  {isActive ? tab.solidIcon : tab.outlineIcon}
                  {tab.badge && dueCount > 0 && (
                    <span className="nav-badge">{dueCount > 99 ? '99+' : dueCount}</span>
                  )}
                </span>
                <span className="text-[10px] font-medium font-ui tracking-wide">{t(tab.labelKey)}</span>
              </NavLink>
            )
          })}
        </div>
      </div>
    </nav>
  )
}
